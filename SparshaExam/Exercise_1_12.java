package Day3;

public class Exercise_1_12 {
    public static void main(String[] args) {
        // Declare a string variable to hold the comb-shaped pattern
        String comb = "+--+--+--+";
        // Declare a string variable to hold the bottom line pattern
        String bottomLine = "+--+--+--+\n";

        // Print the comb-shaped pattern three times
        System.out.println(comb);
        System.out.println("|  |  |  |");
        System.out.println(comb);
        System.out.println("|  |  |  |");
        System.out.println(comb);
        System.out.println("|  |  |  |");
        // Print the bottom line pattern once
        System.out.println(bottomLine);
    }

}
