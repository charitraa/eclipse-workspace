package Day3;

public class Exercise_6 {

  public static void main(String[] args) {
    // declare the variables
    int num = 17;

    // (compare the integer
    if (num % 2 == 0)

      System.out.println("17 is even");

    else
      System.out.println("17 is odd");
  }
}
